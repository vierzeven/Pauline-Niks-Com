            <div class="intro">A Free Man is a work created for the ING Unseen talent awards 2018. Here you can see a selection of the research I did on making and owning weapons.
                A Free Man is about home-made firearms, the liberty to share information and the freedom to make what you want to make. It all started with an article about Cody Wilson founder of the organisation Defense Distributed.
                Wilson wants to gain the ultimate freedom to share information, just as Assange and Snowden once did. To reach his goal he makes it possible for people to print their own 3D guns using plans that they can download online. He chose to share the plans for printing guns because this was an easy way to grab people’s attention for his ideas.
                For the ING Unseen Talent Award I visited different people that make their own weapons. Each individual had their own vision on how this contributes to their freedom. The project deals with the fear of – and liberation from – those who hold power.
